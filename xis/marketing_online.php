<h4>
<span class="txtnegrito">Social Media Optimization</span> - SMO  significa melhorar a apresentação da sua marca e/ou site nas mídias sociais.  As técnicas de <span class="txtnegrito">SMO</span> variam desde a criação de perfis empresariais nas mais diversas redes sociais, blogs, microblogs, fóruns, etc., passando pela integração do site às mídias sociais, até a implementação de mashups dentro do próprio site. Com essas técnicas, o site pode ganhar mais visitas, mais visualização de páginas, ganhar mais publicidade, além de fidelizar seu público dentro das <span class="txtnegrito">mídias sociais.</span>
<br /><br />
No passado, a única forma de se encontrar um site era através de publicidade/parcerias ou dos buscadores. Hoje, com uma boa campanha de mídias sociais, pode-se conseguir muitos clientes e usuários. Mas não basta que a empresa esteja presente nas mídias sociais, é preciso que aja uma verdadeira integração entre o site e as mídias sociais.
</h4>

