<h4>
A maioria das pessoas que usam internet utilizam ferramentas de busca como o Google, o Cadê e o Yahoo quando querem encontrar alguma informação. Para isso Basta escrever a palavra (ou palavras) que se procura, clicar em pesquisar e pronto: você recebe uma lista com vários links relacionados com as palavras que pesquisou. 
<br /><br />
Agora imagine alguém procurando por um Salão de Festas. Que o termo é relevante para a pesquisa naquele momento? E se a pessoa que esta realizando a busca quisesse gastar, não seria ótimo para o dono de um Salão de Festas que o Salão dele aparecesse logo nos primeiros resultados? Ou aparecesse com visibilidade maior que os outros links? Para ajudar no posicionamento dos links nas ferramentas de busca surgiu o <span class="txtnegrito">SEM:</span> Search Engine Marketing.
<br /><br />
<span class="txtnegrito">O Que é o "Search Engine Marketing – SEM"?</span>
<br /><br />
Em sua tradução para o português quer dizer Marketing de Otimização em Buscadores. O SEM são as estratégias de Marketing via Ferramenta de Busca. Ele é baseado na palavra-chave. Quando se busca uma Salão de Festas como no exemplo, a pessoa procura por “Salão de Festas” e recebe a lista de links relacionados. Ele pode procurar também por “Espaço para festas”, “Local para eventos” ou qualquer outro termo relacionado ao seguimento. A lista pode ser infinita, por isto a importância da palavra-chave no <span class="txtnegrito">Search Engine Marketing- SEM.
<br /><br />
Vantagens do Search Engine Marketing sobre as publicidades tradicionais.</span>
<br /><br />
<span class="txtnegrito">- Relevância:</span>  a pessoa vê a publicidade quando está procurando o assunto;<br />
<span class="txtnegrito">- Visibilidade:</span> As ferramentas de busca estão entre os site mais famosos de internet, sendo a página inicial de navegação de muitas pessoas.
<br /><br />
O <span class="txtnegrito">Search Engine Marketing</span> é mais uma alternativa para você conseguir público selecionado para o seu site. Consiste em um conjunto de estratégias sendo um trabalho a longo prazo.
</h4>

